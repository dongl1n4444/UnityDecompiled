namespace UnityEngine
{
    using System;

    /// <summary>
    /// <para>Fog mode to use.</para>
    /// </summary>
    public enum FogMode
    {
        /// <summary>
        /// <para>Exponential fog.</para>
        /// </summary>
        Exponential = 2,
        /// <summary>
        /// <para>Exponential squared fog (default).</para>
        /// </summary>
        ExponentialSquared = 3,
        /// <summary>
        /// <para>Linear fog.</para>
        /// </summary>
        Linear = 1
    }
}

