namespace UnityEngine
{
    using System;

    /// <summary>
    /// <para>Color space for player settings.</para>
    /// </summary>
    public enum ColorSpace
    {
        /// <summary>
        /// <para>Gamma color space.</para>
        /// </summary>
        Gamma = 0,
        /// <summary>
        /// <para>Linear color space.</para>
        /// </summary>
        Linear = 1,
        /// <summary>
        /// <para>Uninitialized color space.</para>
        /// </summary>
        Uninitialized = -1
    }
}

